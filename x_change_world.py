# ── Transformation prose scanner ─────────────────────────────────────────────
def scan_transformation_prose(text, rs=None):
    """
    Scan model response prose after a transformation turn.
    Extracts body measurements without requiring structured tag output.
    Returns dict with any of: height, weight, bust, build.
    """
    import re
    result = {}

    # Height — 5'1", 5'2"
    m = re.search(r"(\d+)'(\d+)", text)
    if m:
        result['height'] = m.group(1) + "'" + m.group(2) + '"'

    # Weight — 100lbs, 105 lbs
    m = re.search(r'(\d+)\s*lbs?', text, re.IGNORECASE)
    if m:
        result['weight'] = m.group(1) + 'lbs'

    # Bust — B-cup, B cup, DD cup, A cups
    m = re.search(r'\b([A-Fa-f]{1,2}(?:\+|-)?)[\s-]?cups?\b', text, re.IGNORECASE)
    if m:
        letter = m.group(1).rstrip('-').upper()
        result['bust'] = letter + '-cup'

    # Build keywords
    build_kws = (rs or {}).get('card_scan', {}).get('build_keywords', [
        'petite', 'slim', 'slender', 'slight', 'lean', 'athletic', 'toned',
        'muscular', 'curvy', 'curvaceous', 'hourglass', 'full', 'delicate',
        'proportional', 'heavy', 'stocky', 'broad',
    ])
    found = [k for k in build_kws if re.search(r'\b' + k + r'\b', text, re.IGNORECASE)]
    if found:
        result['build'] = ', '.join(found[:3])

    return result

def handle_response(assistant_text: str, state: dict, events: dict, rs: dict) -> None:
    """
    Called by the proxy after the model responds.
    Handles anything that needs to happen based on the response content.
    Currently: scans prose for body measurements on transformation turns.
    Add any future response-side logic here — proxy stays untouched.
    """
    if events.get('pill_taken'):
        body_result = scan_transformation_prose(assistant_text, rs)
        # If model didn't state numbers, fall back to target body from pill descriptor
        if not body_result or not any(k in body_result for k in ('height', 'weight', 'bust')):
            desc     = events.get('pill_descriptor') or {}
            color    = desc.get('color', '')
            modifier = desc.get('body_modifier')
            if modifier and color:
                targets = (rs.get('body_modifiers', {}).get(color, {})
                           .get('modifiers', {}).get(modifier, {}))
                if targets:
                    fallback = {}
                    if 'height' in targets: fallback['height'] = targets['height'].split('-')[0].strip()
                    if 'weight' in targets: fallback['weight'] = targets['weight'].split('-')[0].strip()
                    if 'bust'   in targets: fallback['bust']   = targets['bust'].split(',')[0].strip()
                    if 'build'  in targets: fallback['build']  = targets['build'].split(',')[0].strip()
                    if fallback:
                        body_result = {**fallback, **(body_result or {})}
                        print('[BODY_SCAN] fallback to target body:', body_result)
        if body_result:
            state['resolved_body'] = body_result



def process_turn(system_text: str, messages: list, state: dict, persona_state: dict,
                 persona: str, session_key: str, scan_last: int = 12,
                 debug: bool = False, rs: dict = None) -> dict:
    """
    Single entry point for the proxy to call each turn.
    Takes raw request data, returns everything the proxy needs to forward the request.

    Returns dict:
        name        — character name
        sex         — card sex
        state       — updated state
        persona_state — updated persona state
        header      — injection block string
        ok          — False if request should passthrough (missing name/sex)
    """
    name = extract_card_name(system_text, rs)
    sex  = extract_card_sex(system_text, rs)
    if not name or not sex:
        return {'ok': False}

    # Regen/swipe detection — hash the last user message.
    # If it matches last turn's hash, this is a regenerate: skip increment and reuse prior state.
    last_user_msg = next(
        (m.get('content', '') for m in reversed(messages) if m.get('role') == 'user'), ''
    )
    import hashlib as _hl
    last_hash = _hl.md5(last_user_msg[:500].encode('utf-8', errors='ignore')).hexdigest()
    is_regen  = (last_hash == state.get('_last_user_hash'))
    if not is_regen:
        state['turn']           = int(state.get('turn', 0)) + 1
        state['_last_user_hash'] = last_hash

    # Stat generation — first turn only
    flags        = state.setdefault('flags', {})
    statgen_flag = rs.get('engine_config', {}).get('statgen_flag', 'statgen_done')
    if not flags.get(statgen_flag):
        persona_lore_stats = rs.get('personas', {}).get(name)
        if persona_lore_stats:
            state['stats']     = dict(persona_lore_stats)
            state['card_body'] = scan_card_body(system_text, rs)
            print('[STATGEN] Persona stats used for ' + name)
        else:
            card_text  = _card_section(system_text)
            stats_line = extract_card_stats(card_text, rs)
            parsed     = parse_card_stats(stats_line, rs) if stats_line else None
            if parsed:
                state['stats'] = parsed
                print('[STATGEN] Card stats used for ' + name + ': ' + stats_line)
            else:
                state['stats'] = run_statgen(card_text, rs)
                print('[STATGEN] Keyword fallback for ' + name)
            state['card_body'] = scan_card_body(system_text, rs)
        flags[statgen_flag] = True
        if state['card_body']:
            print('[BODY] ' + str(state['card_body']))

    if is_regen:
        if debug:
            print('[PROXY] regen detected — skipping turn increment and event processing')
        return {
            'ok':            True,
            'name':          name,
            'sex':           sex,
            'state':         state,
            'persona_state': persona_state,
            'events':        {},
            'header':        build_header(name, sex, state, [], {}, rs, persona, persona_state),
        }

    # Arousal scan — last 2 chat messages only, skip turn 1
    recent = [m for m in messages[-scan_last:]
              if isinstance(m, dict) and m.get('role') != 'system']
    persona_markers = tuple(rs['card_format'].get('strip_markers', []))
    chat_only = [
        m for m in recent
        if m.get('role') in ('user', 'assistant')
        and not any(marker in (m.get('content') or '') for marker in persona_markers)
    ]
    arousal_scan_msgs = chat_only[-2:] if len(chat_only) >= 2 else chat_only
    if state.get('turn', 1) > 1:
        apply_arousal_decay(state, rs)
        apply_effect_passives(state, rs, debug=debug)
        scan_arousal(arousal_scan_msgs, state, rs, debug=debug)

    # Persona stats from lore
    persona_state['stats'] = rs.get('personas', {}).get(persona, persona_state.get('stats', {}))

    # Event detection and state processing
    notes = []
    recent_for_intake = (recent[:-1] if recent and recent[-1].get('role') == 'assistant'
                         else recent)
    events = detect_events(recent_for_intake, state, sex, rs, debug=debug)
    state  = process_events(state, events, sex, notes, rs)

    persona_state = update_persona_relationship(persona_state, name, events, state, rs)

    # Clean up one-turn buffs
    denial_buff_stat = state.pop('_denial_buff_stat', None)
    if denial_buff_stat:
        state['stats'][denial_buff_stat] = get_stat_mod(state, denial_buff_stat) - 1

    if debug:
        debug_print(session_key, name, sex, state, events, rs, persona, persona_state)

    header = build_header(name, sex, state, notes, events, rs, persona, persona_state)
    if debug:
        print('[INJECT]\n' + header + '\n')

    return {
        'ok':           True,
        'name':         name,
        'sex':          sex,
        'state':        state,
        'persona_state': persona_state,
        'events':       events,
        'header':       header,
    }


