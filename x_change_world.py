def inject_header(req_json: dict, header: str, max_inject: int, rs: dict) -> dict:
    """Inject the state header into the last user message. Fully lore-owned."""
    if not header or not header.strip():
        return req_json
    messages = req_json.get('messages', [])
    if not isinstance(messages, list):
        return req_json
    header = header[:max_inject]
    ec     = rs.get('engine_config', {})
    prefix = ec.get('injection_prefix', '\n\n[STATE]\n')
    suffix = ec.get('injection_suffix', '\n[/STATE]')
    for i in range(len(messages) - 1, -1, -1):
        if messages[i].get('role') == 'user':
            existing = messages[i].get('content') or ''
            messages[i]['content'] = existing + prefix + header + suffix
            req_json['messages'] = messages
            return req_json
    req_json['messages'] = [{'role': 'system', 'content': header}] + messages
    return req_json


