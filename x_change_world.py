def run_roll(state, name, notes, rs):
    if is_locked(state, name):
        notes.append(name + ':LOCKED')
        return 'locked'

    mech        = rs['effect_mechanics'].get(name, {})
    roll_list   = mech.get('roll', [])
    mods        = compute_mods(state, roll_list)
    current_dc  = get_effect_dc(state, name, rs)
    escalation  = int(mech.get('escalation', 2))
    escalate_on = mech.get('escalate_on', 'pass')

    ar_pen = arousal_roll_penalty(state, rs)
    if ar_pen == 'auto_fail':
        notes.append(name + ':auto-fail(arousal=max)')
        return 'fail'
    mods += ar_pen

    max_possible = 20 + mods
    if max_possible < current_dc:
        lock_effect(state, name)
        notes.append(name + ':LOCKED(max=' + str(max_possible) + '<DC=' + str(current_dc) + ')')
        return 'locked'

    roll   = random.randint(1, 20)
    total  = roll + mods
    passed = total >= current_dc
    tag    = 'PASS' if passed else 'FAIL'
    entry  = name + ':' + tag + '(d20=' + str(roll) + ('+' if mods >= 0 else '') + str(mods) + '=' + str(total) + ' vs DC=' + str(current_dc) + ')'
    notes.append(entry)
    state.setdefault('roll_log', []).append(entry)
    print('[ROLL] ' + name + ' ' + tag + ' d20=' + str(roll) + ' mods=' + str(mods) + ' total=' + str(total) + ' dc=' + str(current_dc))

    if passed and escalate_on == 'pass':
        set_effect_dc(state, name, current_dc + escalation)
        check_math_lock(state, name, rs)
    if not passed and escalate_on == 'fail':
        set_effect_dc(state, name, current_dc + escalation)
        check_math_lock(state, name, rs)

    tb      = mech.get('trigger_behavior', {})
    tb_log  = tb.get('on_pass') if passed else tb.get('on_fail')
    if tb_log:
        notes.append(tb_log)

    return 'pass' if passed else 'fail'

def advance_stage(state, name, notes, rs, to_db=False):
    mech      = rs['effect_mechanics'].get(name, {})
    stages    = mech.get('stages', {})
    current   = get_effect_stage(state, name)
    max_stage = len(stages)
    if current >= max_stage:
        return
    new_stage  = current + 1
    set_effect_stage(state, name, new_stage)
    stage_info = stages.get(str(new_stage), {})
    notes.append(name + ' stage->' + str(new_stage) + ': ' + stage_info.get('note', ''))
    stats      = state.setdefault('stats', {})
    ec         = (rs or {}).get('engine_config', {})
    pen_suffix = ec.get('stage_penalty_suffix', '_penalty')
    perm_prefix = ec.get('perm_stage_flag_prefix', 'perm_')
    for sk, sv in stage_info.items():
        if sk.endswith(pen_suffix):
            base = sk[:-len(pen_suffix)].upper() + '_mod'
            stats[base] = int(stats.get(base, 0)) + int(sv)
            notes.append(base + '=' + str(stats[base]))
    if stage_info.get('permanent') or new_stage >= max_stage:
        lock_effect(state, name)
        notes.append(name + ':permanent stage=' + str(new_stage))
        if to_db:
            state.setdefault('flags', {})[perm_prefix + name + '_stage'] = new_stage

