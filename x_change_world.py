def update_persona_relationship(persona_state, char_name, events, char_state, rs=None):
    schema       = (rs or {}).get('relationship_schema', {})
    default_flds = schema.get('default_fields', {})
    rels = persona_state.setdefault('relationships', {})
    rel  = rels.setdefault(char_name.lower(), dict(default_flds))

    char_effects = char_state.get('active_effects', [])
    flags        = char_state.get('flags', {})

    for event_key, field in schema.get('event_increments', {}).items():
        if events.get(event_key):
            rel[field] = rel.get(field, 0) + 1

    for flag_key, setter in schema.get('flag_sets', {}).items():
        if flags.get(flag_key):
            rel[setter['field']] = setter['value']

    orgasm_field  = schema.get('orgasm_increment', '')
    orgasm_events = schema.get('orgasm_events', [])
    orgasm_reqs   = schema.get('orgasm_requires_any_effect', {})
    orgasm_fired  = False
    for ev in orgasm_events:
        if not events.get(ev):
            continue
        req = orgasm_reqs.get(ev)
        if req and not any(e in char_effects for e in req):
            continue
        orgasm_fired = True; break
    if orgasm_fired:
        rel[orgasm_field] = rel.get(orgasm_field, 0) + 1

    for event_key, setter in schema.get('event_sets', {}).items():
        if events.get(event_key):
            field = setter['field']
            if setter.get('only_if_unset') and rel.get(field):
                continue
            rel[field] = events[event_key] if setter.get('from_event') else setter.get('value')

    dynamics = schema.get('dynamics', {})
    for eff, dynamic_val in dynamics.items():
        if eff in char_effects:
            rel['dynamic'] = dynamic_val
            break

    return persona_state


