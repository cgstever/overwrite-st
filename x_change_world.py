# ── Conditional flavor ───────────────────────────────────────────────────────

def evaluate_conditional_flavor(state, events, rs):
    blocks  = []
    flavor  = rs.get('conditional_flavor', [])
    effects = state.get('active_effects') or []
    arousal = float(state.get('arousal', 0))
    flags   = state.get('flags') or {}
    stages  = state.get('effect_stages') or {}
    locks   = state.get('effect_locks') or {}

    for entry in flavor:
        cond = entry.get('condition', '')
        text = entry.get('text', '').strip()
        if not text:
            continue
        matched = False

        if cond == 'effect_active':
            matched = entry.get('effect') in effects
        elif cond == 'effect_locked':
            eff = entry.get('effect')
            matched = eff in effects and bool(locks.get(eff))
        elif cond == 'effect_stage_gte':
            eff = entry.get('effect'); stage = int(entry.get('stage', 1))
            matched = eff in effects and stages.get(eff, 0) >= stage
        elif cond == 'effect_stage_eq':
            eff = entry.get('effect'); stage = int(entry.get('stage', 1))
            matched = eff in effects and stages.get(eff, 0) == stage
        elif cond == 'arousal_gte':
            matched = arousal >= float(entry.get('threshold', 0))
        elif cond == 'arousal_lt':
            matched = arousal < float(entry.get('threshold', 60))
        elif cond == 'arousal_range':
            lo = float(entry.get('min', 0)); hi = float(entry.get('max', 60))
            matched = lo <= arousal < hi
        elif cond == 'event':
            matched = bool(events.get(entry.get('event_key', '')))
        elif cond == 'event_and_effect':
            matched = bool(events.get(entry.get('event_key', ''))) and entry.get('effect') in effects
        elif cond == 'flag':
            matched = bool(flags.get(entry.get('flag', '')))
        elif cond == 'pill_active':
            matched = state.get('active_pill') == entry.get('pill')
        elif cond == 'any_pill_active':
            matched = bool(state.get('active_pill'))
        elif cond == 'effects_combo':
            matched = all(e in effects for e in entry.get('effects', []))
        elif cond == 'effects_combo_arousal':
            alo = float(entry.get('arousal_min', 0)); ahi = float(entry.get('arousal_max', 65))
            matched = all(e in effects for e in entry.get('effects', [])) and alo <= arousal < ahi
        elif cond == 'effects_combo_arousal_counter':
            alo = float(entry.get('arousal_min', 0)); ahi = float(entry.get('arousal_max', 65))
            counter = entry.get('counter', ''); c_val = int(state.get(counter, 0))
            c_eq = entry.get('counter_eq'); c_gte = entry.get('counter_gte')
            c_match = True
            if c_eq  is not None: c_match = c_val == int(c_eq)
            if c_gte is not None: c_match = c_val >= int(c_gte)
            matched = all(e in effects for e in entry.get('effects', [])) and alo <= arousal < ahi and c_match
        elif cond == 'effect_stage_range_event':
            eff = entry.get('effect', ''); s_min = int(entry.get('stage_min', 0)); s_max = int(entry.get('stage_max', 99))
            ev = entry.get('event_key', ''); stage = stages.get(eff, 0)
            matched = s_min <= stage <= s_max and bool(events.get(ev))
        elif cond == 'effects_combo_flag':
            required = entry.get('effects', []); flag = entry.get('flag', ''); flag_f = entry.get('flag_false', '')
            s_lt = entry.get('addiction_stage_lt')
            stage_ok = True
            if s_lt is not None:
                stage_ok = stages.get(entry.get('stage_effect', ''), 0) < int(s_lt)
            matched = all(e in effects for e in required) and bool(flags.get(flag)) and not bool(flags.get(flag_f)) and stage_ok
        elif cond == 'effects_combo_flag_stage':
            required = entry.get('effects', []); flag = entry.get('flag', ''); flag_f = entry.get('flag_false', '')
            s_gte = int(entry.get('stage_gte', 1)); stage_eff = entry.get('stage_effect', '')
            matched = all(e in effects for e in required) and bool(flags.get(flag)) and not bool(flags.get(flag_f)) and stages.get(stage_eff, 0) >= s_gte
        elif cond == 'flag_and_no_effect':
            flag = entry.get('flag', ''); eff = entry.get('effect', '')
            s_lt = entry.get('addiction_stage_lt', 99); stage_eff = entry.get('stage_effect', '')
            matched = bool(flags.get(flag)) and eff not in effects and stages.get(stage_eff, 0) < int(s_lt)
        elif cond == 'flag_stage_range':
            flag = entry.get('flag', ''); s_eff = entry.get('stage_effect', '')
            s_min = int(entry.get('stage_min', 0)); s_max = int(entry.get('stage_max', 99))
            matched = bool(flags.get(flag)) and s_min <= stages.get(s_eff, 0) <= s_max
        elif cond == 'flag_permanent_stage':
            flag = entry.get('flag', ''); s_eff = entry.get('stage_effect', ''); s_gte = int(entry.get('stage_gte', 3))
            matched = bool(flags.get(flag)) and stages.get(s_eff, 0) >= s_gte
        elif cond == 'flag_false':
            matched = not bool(flags.get(entry.get('flag', '')))
        elif cond == 'flag_true':
            matched = bool(flags.get(entry.get('flag', '')))
        elif cond == 'effect_not_active_list':
            matched = not any(e in effects for e in entry.get('effects', []))

        if matched and 'also' in entry:
            also = entry['also']; also_cond = also.get('condition', ''); also_match = False
            if also_cond == 'arousal_range':
                lo = float(also.get('min', 0)); hi = float(also.get('max', 65))
                also_match = lo <= arousal < hi
            elif also_cond == 'flag_true':
                also_match = bool(flags.get(also.get('flag', '')))
            elif also_cond == 'flag_false':
                also_match = not bool(flags.get(also.get('flag', '')))
            elif also_cond == 'any_pill_active':
                also_match = bool(state.get('active_pill'))
            elif also_cond == 'effect_active':
                also_match = also.get('effect', '') in effects
            elif also_cond == 'effect_not_active_list':
                also_match = not any(e in effects for e in also.get('effects', []))
            elif also_cond == 'event':
                also_match = bool(events.get(also.get('event_key', '')))
            elif also_cond == 'effect_stage_gte':
                eff = also.get('effect', ''); stg = int(also.get('stage', 1))
                also_match = eff in effects and stages.get(eff, 0) >= stg
            matched = also_match

        if matched:
            blocks.append(text)

    return blocks


# ── Pill detection ───────────────────────────────────────────────────────────

def find_pill_ingest(messages_recent, rs, state=None):
    """
    Scan messages for pill intake.
    - If a pill offer is seen, store it in state['_pending_pill'].
    - If an intake verb is seen and state has a pending pill, fire on that pill.
    - No requirement that color and intake verb appear in the same message.
    """
    offer_re    = rs.get('pill_offer_re')
    intake_re   = rs.get('intake_verb_re')
    negation_re = rs.get('pill_negation_re')
    false_pos_re = rs.get('pill_false_pos_re')
    if not offer_re or not intake_re:
        return None, [], None

    # Seed pending from persisted state
    pending_color = (state or {}).get('_pending_pill')
    pending_text  = (state or {}).get('_pending_pill_text')

    for msg in messages_recent:
        text = (msg.get('content') or '').strip()
        role = (msg.get('role') or '').lower()
        if not text:
            continue

        # Update pending pill whenever an offer is seen
        m = offer_re.search(text)
        if m:
            # color may be in group 1 or group 2 depending on which arm matched
            color = (m.group(1) or m.group(2) or '').lower()
            if color and color != pending_color:
                pending_color = color
                pending_text  = m.group(0)
                if state is not None:
                    desc = parse_pill_descriptor(pending_text, rs)
                    desc['color'] = pending_color
                    state['_pending_pill']      = pending_color
                    state['_pending_pill_desc'] = desc
                    state.pop('_pending_pill_text', None)

        # Intake: only fire on user messages — assistant dialogue must never consume the pill
        if pending_color and role == 'user' and intake_re.search(text):
            ingested = False
            for vm in intake_re.finditer(text):
                pre  = text[max(0, vm.start()-40):vm.start()]
                post = text[vm.end():min(len(text), vm.end()+40)]
                if negation_re and (negation_re.search(pre) or negation_re.search(post)):
                    continue
                if false_pos_re and false_pos_re.match(post):
                    continue
                ingested = True; break
            if ingested:
                # Clear pending from state — pill is now consumed
                if state is not None:
                    state.pop('_pending_pill', None)
                    state.pop('_pending_pill_text', None)
                return pending_color, list(messages_recent), pending_text

    return None, [], None

def parse_pill_from_ingest(messages_recent, rs, state=None, debug=False):
    color, context, stored_desc_text = find_pill_ingest(messages_recent, rs, state=state)
    if not color:
        return None, None
    # Use pre-parsed descriptor from state if available, else parse now
    descriptor = (state or {}).pop('_pending_pill_desc', None)
    if not descriptor:
        pill_phrase = stored_desc_text or color
        descriptor  = parse_pill_descriptor(pill_phrase, rs)
        descriptor['color'] = color
    if debug:
        print('[PILL] effects: ' + str(descriptor.get('effects')))
    return color, descriptor


# ── Event detection ──────────────────────────────────────────────────────────

def detect_events(messages_recent, state, card_sex, rs, debug=False):
    events   = {}
    all_text = '\n'.join((m.get('content') or '') for m in messages_recent)
    user_text = '\n'.join(
        (m.get('content') or '') for m in messages_recent if m.get('role') == 'user'
    )
    # Most recent user message only — for one-shot events that must not repeat
    last_user_text = next(
        ((m.get('content') or '') for m in reversed(messages_recent) if m.get('role') == 'user'), ''
    )

    if not state.get('active_pill'):
        color, descriptor = parse_pill_from_ingest(messages_recent, rs, state=state, debug=debug)
        if color:
            events['pill_taken']      = color
            events['pill_descriptor'] = descriptor
            if descriptor:
                if descriptor.get('body_modifier'):
                    state['_pending_body_modifier'] = descriptor['body_modifier']
                for eff in descriptor.get('effects', []):
                    events['flavor_' + eff] = True

    active_effects = state.get('active_effects') or []
    for ft in rs['flavor_triggers']:
        if ft.get('requires_pill'):
            continue
        if ft['effect'] in active_effects:
            continue
        for kw in ft.get('keywords', []):
            if re.search(r'\b' + re.escape(kw) + r'\b', all_text, re.IGNORECASE):
                events['flavor_' + ft['effect']] = True
                break

    temp = dict(state)
    if events.get('pill_taken'):
        temp['active_pill'] = events['pill_taken']

    for event_name, compiled in rs.get('event_patterns_compiled', {}).items():
        # user_only events check ONLY the most recent user message to prevent
        # the same message re-matching across multiple turns in the scan window
        if compiled['source'] in ('user_only', 'last_user'):
            text = last_user_text
        elif compiled['source'] == 'last_assistant':
            text = next(((m.get('content') or '') for m in reversed(messages_recent) if m.get('role') == 'assistant'), '')
        else:
            text = all_text
        if not compiled['re'].search(text):
            continue
        if compiled['requires_form_eligible']:
            if not can_get_pregnant(temp, card_sex, rs):
                if debug:
                    print('[EVENT] ' + event_name + ' not eligible (form)')
                continue
        req_flag = compiled['requires_flag']
        if req_flag and not state.get('flags', {}).get(req_flag):
            continue
        events[event_name] = True

    return events


